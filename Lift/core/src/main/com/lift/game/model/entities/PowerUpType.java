package com.lift.game.model.entities;

/**
 * Types of power ups that exist.
 */
public enum PowerUpType {
    /**
     * Gives a life.
     */
    LifePowerUp,
    /**
     * Increases the elevator velocity.
     */
    ElevatorVelocity,
    /**
     * Null power up.
     */
    NullPowerUp,
    /**
     * Grants coins.
     */
    CoinPowerUp
}
